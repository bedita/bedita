// Load default values for object types cache, if missing.
if (!Configure::check('Cache._bedita_object_types_')) {
    Configure::write('Cache._bedita_object_types_', [
        'className' => 'File',
        'prefix' => 'bedita_object_types_',
        'path' => CACHE . 'object_types/',
        'serialize' => true,
        'duration' => '+1 year',
    ]);
